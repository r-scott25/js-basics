//  **** Objects *****

let person = {
  name: "Rachel",
  age: 41,
};

console.log(person);

//  Access properties in objects using Dot Notation or Bracket Notation

// Dot Notation
person.name = "Lynn";

console.log(person);

console.log(person.name);

// Bracket Notation
person["name"] = "Scott";

console.log(person.name);
