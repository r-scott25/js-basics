// ***** Arrays *****

// Use arrays to store lists

// index of first element is 0, index of second element is 1, and so on

let selectedColors = ["red", "blue"];

console.log(selectedColors);

// index of red = 0    index of blue = 1

// To access an element in an array, use the index inside square brackets:

console.log(selectedColors[0]);
console.log(selectedColors[1]);

// Add elements to the array: Indicate the index number where the new element will appear

selectedColors[2] = "green";
console.log(selectedColors);
// now console reads: ['red', 'blue', 'green']
