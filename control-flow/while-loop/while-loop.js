// While Loop

// Difference between a for loop and a while loop:
// In a for loop, the loop variable is part of the loop variable itself.
// In a while loop, you have to declare this while variable externally.

// let variable = value;
// while(condition) {
//   statement
// }

let i = 0;
while (i <= 5) {
  if (i % 2 !== 0) {
    console.log(i);
    i++;
  }
}
